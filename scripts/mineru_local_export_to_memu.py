#!/usr/bin/env python3
"""
Ingest locally exported MinerU extraction results into MemU (RAG) only.

Flow:
1) Discover local extracted directories from export state file (preferred)
   or by scanning output root for *_extracted directories
2) Read and merge textual artifacts from extracted files
3) Chunk text and write chunks to MemU memory
4) Optionally ingest extracted images via MemU file endpoint
5) Persist progress state for resume/retry
"""

from __future__ import annotations

